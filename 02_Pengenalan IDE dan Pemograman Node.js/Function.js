//named function
function perkalian(a, b) {
    let result = a*b
    return result
    
}

// ekspresif function //arrow function
const pertambahan = (a, b) => {
    let result = a+b
    return result
}

// anonymous function
const pembagian = function bagi(a, b) {
    let result = a/b
    return result
}

console.log(perkalian(5, 2))
console.log(pertambahan(5, 2))
console.log(pembagian(10, 2))

